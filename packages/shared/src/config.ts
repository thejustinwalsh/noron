/**
 * Load a BenchdConfig from a TOML file.
 * Returns null if the file does not exist.
 * Missing keys are filled from DEFAULT_CONFIG.
 */
export function loadConfig(path: string = CONFIG_PATH): BenchdConfig | null {
	if (!existsSync(path)) return null;

	try {
		const content = readFileSync(path, "utf-8");
		const parsed = parseToml(content);
		const config = { ...DEFAULT_CONFIG };

		for (const [tomlKey, value] of Object.entries(parsed)) {
			const configKey = KEY_MAP[tomlKey];
			if (configKey) {
				(config as Record<string, unknown>)[configKey] = value;
			}
		}

		return config;
	} catch {
		return null;
	}
}

/**
 * Write a BenchdConfig to a TOML file.
 * Creates parent directories if they don't exist.
 */
export function writeDefaultConfig(
	path: string,
	config: BenchdConfig,
): void {
	const dir = dirname(path);
	if (!existsSync(dir)) {
		mkdirSync(dir, { recursive: true });
	}
	writeFileSync(path, serializeToml(config), "utf-8");
}
