import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";

/** Runtime configuration for benchd, persisted as TOML */
export interface BenchdConfig {
	isolatedCores: number[];
	housekeepingCore: number;
	targetTempC: number;
	thermalHistorySize: number;
	thermalPollIntervalMs: number;
	thermalTimeoutMs: number;
	thermalMarginC: number;
	thermalBaselineSettlingS: number;
	socketPath: string;
	benchmarkSlice: string;
	benchmarkCgroup: string;
	benchTmpfs: string;
	lockDisconnectGraceMs: number;
	tokenExpiryHours: number;
	jobTimeoutMs: number;
	updateRepo: string;
	updateAuto: boolean;
	updateCheckIntervalHours: number;
	runnerLabel: string;
}

/** Default path for the config file */
export const CONFIG_PATH = "/etc/benchd/config.toml";

